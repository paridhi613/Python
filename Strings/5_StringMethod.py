name = "paridhi kalyane"

print(name.capitalize())    #makes the first letter capital

print(name.title())    #makes the first letter capital of every word

print(name.upper())   #makes each letter capital of the string

print(name.lower())   #makes each letter in lowercase 

print(name.find("k"))   #return the lowest index of the letter

print(name.count("i"))   #gives the count of the letter present in the string



print(name.find("z"))    #gives -1 if the letter is not found
print(name.count("z"))    #gives 0 if the letter if not found



print(name.index("i"))   #return the lowest index of the letter

print(name.replace("a", "@"))  #replaces the old text to new one 

print(name.split())  #splits the string from the space into two
print(name.split("a"))  #splits the string from a

print(name.isupper())    #returns true if the whole string is in uppercase
print("PARIDHI".isupper())   #returns true coz the string is in uppercase

print(name.islower())   #returns true if the whole string is in lowecase


